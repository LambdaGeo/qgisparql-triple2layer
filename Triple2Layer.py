class Triple2Layer:
    """QGIS Plugin Implementation."""

    def __init__(self, iface) -> None:
        """Initialize the Triple2Layer plugin.

        Args:
            iface: A QGIS interface instance providing the hook to manipulate
                   the QGIS application at runtime.
        """
        # Save reference to the QGIS interface
        self.iface = iface
        # initialize plugin directory
        self.plugin_dir = os.path.dirname(__file__)
        # initialize locale
        locale = QSettings().value('locale/userLocale')[0:2]
        locale_path = os.path.join(
            self.plugin_dir,
            'i18n',
            'Triple2Layer_{}.qm'.format(locale))

        if os.path.exists(locale_path):
            self.translator = QTranslator()
            self.translator.load(locale_path)
            QCoreApplication.installTranslator(self.translator)

        self.actions = []
        self.menu = self.tr(u'&QGISSPARQL')

        # Check if plugin was started the first time in current QGIS session
        # Must be set in initGui() to survive plugin reloads
        self.first_start = None
        

    # noinspection PyMethodMayBeStatic
    def tr(self, message):
        # noinspection PyTypeChecker,PyArgumentList,PyCallByClass
        return QCoreApplication.translate('Triple2Layer', message)

    def add_action(
            self,
            icon_path: str,
            text: str,
            callback,
            enabled_flag: bool = True,
            add_to_menu: bool = True,
            add_to_toolbar: bool = True,
            status_tip: str = None,
            whats_this: str = None,
            parent=None) -> QAction:
        """Add a toolbar icon and menu entry for the plugin.

        Args:
            icon_path: Path to the icon for this action.
            text: Text that should be shown in menu items for this action.
            callback: Function to be called when the action is triggered.
            enabled_flag: A flag indicating if the action should be enabled.
            add_to_menu: Flag indicating whether the action should be added to the menu.
            add_to_toolbar: Flag indicating whether the action should be added to the toolbar.
            status_tip: Optional text to show in a popup when the mouse pointer
                        hovers over the action.
            whats_this: Optional text to show in the status bar when the mouse
                        pointer hovers over the action.
            parent: Parent widget for the new action.

        Returns:
            The action that was created.
        """

        icon = QIcon(icon_path)
        action = QAction(icon, text, parent)
        action.triggered.connect(callback)
        action.setEnabled(enabled_flag)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            # Adds plugin icon to Plugins toolbar
            self.iface.addToolBarIcon(action)

        if add_to_menu:
            self.iface.addPluginToVectorMenu(
                self.menu,
                action)

        self.actions.append(action)

        return action

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        icon_path = ':/plugins/Triple2Layer/icon.png'
        self.add_action(
            icon_path,
            text=self.tr(u'Triple2Layer'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # will be set False in run()
        self.first_start = True

    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        for action in self.actions:
            self.iface.removePluginVectorMenu(
                self.tr(u'&QGISSPARQL'),
                action)
            self.iface.removeToolBarIcon(action)

    def run(self):
            """Run method that performs all the real work and manages dialog state."""

            # 1. INITIALIZATION: Setup dialog and signal connections (only once per session)
            if self.first_start == True:
                self.first_start = False
                self.dlg = Triple2LayerDialog()
                self.file_name = None
                
                # Setup button connections
                self.dlg.pushButton.clicked.connect(self.execute)
                self.dlg.button_box.rejected.connect(self.close)
                self.dlg.buttonSPARQL.clicked.connect(self.open_sparql)
                self.dlg.actionToken.triggered.connect(self.set_token)
                self.dlg.comboSourceType.textActivated.connect(self.endpoint_defaut)
                
                # Issue 3 - Task 4: Connect preview button if it exists in UI
                self.dlg.buttonPreview.clicked.connect(self.preview_data)

            # 2. STATE RESET: Clear UI elements between runs to avoid data persistence (Issue 3 - Task 2)
            if hasattr(self.dlg, 'lineLayer'):
                self.dlg.lineLayer.clear()
                self.dlg.lineSPARQL.clear()
                
            if hasattr(self.dlg, 'tableAttributes'):
                self.dlg.tableAttributes.setRowCount(0)
                
            # Ensure the default endpoint is loaded for the current source type
            self.endpoint_defaut()

            # Display the dialog
            self.dlg.show()


    def execute(self) -> None:
        """Validate input fields and initiate the import task."""
        
        # Issue 3 - Task 1: Layer name validation
        layer_name = self.dlg.lineLayer.text().strip()
        if not layer_name:
            self.iface.messageBar().pushMessage(
                "Missing Input", 
                "Please enter a valid layer name before starting the import.",
                level=Qgis.Warning, duration=5)
            return

        # Proceed with attribute check and import
        if self.check_attributes():
            self.save_endpoint()
            self.import_layer()
        else:
            # Error message is already handled within check_attributes
            pass

    def set_token(self) -> None:
        """Prompt the user to enter a Data.world API token and store it in the environment."""
        token, ok = QInputDialog.getText(
            self.dlg, "Data.World Token", "Enter with token")
        if (ok and token != ""):
            os.environ['DW_AUTH_TOKEN'] = token

    def update_status_bar(self, progress: float) -> None:
        """Update the QGIS status bar with the current task progress.

        Args:
            progress: Current progress percentage (0 to 100).
        """
        # Shows a message in the bottom-left corner of QGIS
        self.iface.mainWindow().statusBar().showMessage(
            f"Triple2Layer: Importing features... {int(progress)}%"
        )
        # Clear the message when it reaches 100%
        if progress >= 100:
            self.iface.mainWindow().statusBar().clearMessage()

    def check_attributes(self) -> bool:
        """Read the attribute table and store the user's column configuration.

        Returns:
            True if a geometry column has been selected, False otherwise.
        """
        selected_geo = False

        self.geo_column = ""
        self.saveAttrs = []
        self.id_column = "id:integer"
        for row in range(self.dlg.tableAttributes.rowCount()):
            line_edit = self.dlg.tableAttributes.cellWidget(row, 4)
            attr_name = line_edit.text()

            item = self.dlg.tableAttributes.item(row, 3)
            var_name = item.text()

            check_id = self.dlg.tableAttributes.cellWidget(row, 1)
            check_geo = self.dlg.tableAttributes.cellWidget(row, 2)
            check = self.dlg.tableAttributes.cellWidget(row, 0)
            combo_type = self.dlg.tableAttributes.cellWidget(row, 5)

            if check_id.isChecked():
                self.id_column = attr_name

            if check_geo.isChecked():
                self.geo_column = attr_name
                selected_geo = True

            if check.isChecked() or check_id.isChecked():
                self.saveAttrs.append(
                    (attr_name, dic_attr_type[combo_type.currentText()], var_name))

        return selected_geo

    def load_data_world(self, time):
        """Load data from a Data.world dataset using SPARQL."""
        QgsMessageLog.logMessage('Loading data from Data.world...', 'Triple2Layer')

        try:
            ds = dw.query(self.dlg.lineEndpoint.text(),
                      self.sparql, query_type='sparql')

            QgsMessageLog.logMessage('Data successfully loaded from Data.world.', 'Triple2Layer')
            dict = ds.dataframe.to_dict('records')
            return dict
        except HTTPError as e:
            msg = f'HTTP error while connecting to Data.world: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except Timeout:
            msg = 'Connection to Data.world timed out. Check your internet connection.'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except ConnectionError as e:
            msg = f'Could not connect to Data.world: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except Exception as e:
            msg = f'Unexpected error loading from Data.world: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = 'Fail to load data from Data.world: check dataset name and token'
            self.erroOnLoadLayer = True


    def load_triple_store(self, time):
        """Load data from a SPARQL triple store endpoint."""
        try:
            sparql = SPARQLWrapper(self.dlg.lineEndpoint.text())
            sparql.setQuery(self.sparql)
            sparql.setReturnFormat(JSON)
            results = sparql.query().convert()
            return results["results"]["bindings"]
        except SPARQLWrapperException as e:
            msg = f'SPARQL query error: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except HTTPError as e:
            msg = f'HTTP error while connecting to triple store: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except Timeout:
            msg = 'Connection to triple store timed out. Check the endpoint URL.'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = msg
            self.erroOnLoadLayer = True
        except Exception as e:
            msg = f'Unexpected error loading from triple store: {e}'
            QgsMessageLog.logMessage(msg, 'Triple2Layer', level=Qgis.Critical)
            self.errorMessage = 'Fail to load data from triple store: check endpoint URL'
            self.erroOnLoadLayer = True

    def get_value(self, row: dict, attr: str, source: str = 'dw'):
        """Extract attribute value from a query result row.

        Args:
            row: A single result row (dict) from the SPARQL query.
            attr: The attribute/variable name to extract.
            source: Data source type — 'triple' for triple stores, 'dw' for Data.world.

        Returns:
            The attribute value as a string, or None if not found.
        """
        try:
            if source == 'triple':
                return row[attr]['value']
            else:
                return row[attr]
        except KeyError:
            QgsMessageLog.logMessage(
                f"Attribute '{attr}' not found in result row. Available keys: {list(row.keys())}",
                'Triple2Layer', level=Qgis.Warning)
            return None

    def create_layer(self, source: str, time, records: list) -> None:
            """Build a QGIS vector layer using a non-blocking progress update in the status bar.

            Args:
                source: Data source type — 'triple' for triple stores, 'dw' for Data.world.
                time: Elapsed task time (provided by QgsTask).
                records: List of result rows returned by the SPARQL query.
            """
            # Create the memory layer with the specified ID column
            layer = QgsVectorLayer(
                'Polygon?crs=epsg:4326?field=' + self.id_column,
                self.dlg.lineLayer.text(),
                "memory"
            )
            pr = layer.dataProvider()
            layer.startEditing()
            
            # Add attributes defined in the UI table
            attributes = [QgsField(x[0], x[1]) for x in self.saveAttrs]
            pr.addAttributes(attributes)
            layer.updateFields()

            features = []
            total = len(records)

            if total == 0:
                QgsMessageLog.logMessage('No records to import.', 'Triple2Layer', level=Qgis.Warning)
                return

            # Validate geometry attribute of the first record
            first_geom_wkt = self.get_value(records[0], self.geo_column, source)
            geom = QgsGeometry.fromWkt(first_geom_wkt)
            
            if geom.isNull():
                self.erroOnLoadLayer = True
                self.errorMessage = "Invalid geometry attribute. Please select a column with valid WKT."
                self.check_if_imported_layer()
                return

            QgsMessageLog.logMessage(f'Importing {total} features into layer...', 'Triple2Layer')

            # Task 3: Iterating through records and updating the status bar instead of a modal dialog
            for i, row in enumerate(records):
                fet = QgsFeature()
                wkt_value = self.get_value(row, self.geo_column, source)
                fet.setGeometry(QgsGeometry.fromWkt(wkt_value))
                
                attrs = [self.get_value(row, attr[2], source) for attr in self.saveAttrs]
                fet.setAttributes(attrs)
                features.append(fet)

                # Update progress status every 20 features to maintain UI responsiveness
                if i % 20 == 0 or i == total - 1:
                    progress_percent = ((i + 1) / total) * 100
                    self.update_status_bar(progress_percent)
                    # Allow QGIS to process UI events (prevents freezing)
                    QCoreApplication.processEvents()

            # Finalize the layer setup
            layer.addFeatures(features)
            layer.updateExtents()
            layer.commitChanges()
            QgsProject.instance().addMapLayer(layer)

            self.iface.messageBar().pushMessage(
                "Success", f"Layer '{layer.name()}' imported successfully.",
                level=Qgis.Success, duration=3)
            # Finalize the layer setup
            layer.addFeatures(features)
            layer.updateExtents()
            layer.commitChanges()
            QgsProject.instance().addMapLayer(layer)

            self.iface.messageBar().pushMessage(
                "Success", f"Layer '{layer.name()}' imported successfully.",
                level=Qgis.Success, duration=3)
        

    def check_if_imported_layer(self) -> None:
        """Display a warning message in the QGIS message bar if the import failed."""
        self.iface.messageBar().clearWidgets()
        if self.erroOnLoadLayer:
            self.iface.messageBar().pushMessage(
                "Import failed", self.errorMessage,
                level=Qgis.Warning, duration=5)
            


    def import_from_dataworld(self) -> None:
            """Start an asynchronous task to import data from a Data.world dataset."""
            if "DW_AUTH_TOKEN" not in os.environ:
                self.iface.messageBar().pushMessage(
                    "Authentication required", 
                    "Data.world token not set. Please enter your API token.",
                    level=Qgis.Warning, duration=5
                )
                self.set_token()
                return # Exit early if no token is found

            QgsMessageLog.logMessage('Creating Data.world import task...', 'Triple2Layer')
            
            # 1. Create the task
            self.task = QgsTask.fromFunction(
                'Importing a layer', 
                self.load_data_world,
                on_finished=partial(self.create_layer, 'dw')
            )

            # 2. Connect signals (Task 3 - Progress Feedback)
            self.task.progressChanged.connect(self.update_status_bar)
            self.task.taskCompleted.connect(self.check_if_imported_layer)
            
            # 3. Add to manager only once
            QgsApplication.taskManager().addTask(self.task)
            

    def preview_data(self) -> None:
            """Run the SPARQL query with a LIMIT 5 to preview results without a full import."""
            source = self.dlg.comboSourceType.currentText()
            endpoint = self.dlg.lineEndpoint.text()

            # Check if the mandatory fields are populated
            if not endpoint or not hasattr(self, 'sparql'):
                self.iface.messageBar().pushMessage(
                    "Preview Error", "Please provide an endpoint and load a SPARQL file first.",
                    level=Qgis.Warning, duration=5)
                return

            # Issue 3 - Task 4: Append LIMIT 5 for a quick connectivity check
            preview_query = f"{self.sparql}\nLIMIT 5"
            
            # Show activity in the status bar
            self.iface.mainWindow().statusBar().showMessage("Fetching preview data...")

            try:
                if source == "Triple Store Endpoint":
                    # Triple Store Preview
                    sparql = SPARQLWrapper(endpoint)
                    sparql.setQuery(preview_query)
                    sparql.setReturnFormat(JSON)
                    records = sparql.query().convert()["results"]["bindings"]
                else:
                    # Data.world Preview
                    ds = dw.query(endpoint, preview_query, query_type='sparql')
                    records = ds.dataframe.to_dict('records')

                self.iface.mainWindow().statusBar().clearMessage()

                if records:
                    # Get a small sample of the first record to show success
                    sample = str(records[0])[:150]
                    self.iface.messageBar().pushMessage(
                        "Preview Success", 
                        f"Connection established! Found {len(records)} sample rows. Sample: {sample}...",
                        level=Qgis.Info, duration=10)
                else:
                    self.iface.messageBar().pushMessage(
                        "Preview", "The query executed successfully but returned no results.", 
                        level=Qgis.Warning, duration=5)

            except Exception as e:
                self.iface.mainWindow().statusBar().clearMessage()
                self.iface.messageBar().pushMessage(
                    "Preview Failed", f"Error connecting to source: {str(e)}", 
                    level=Qgis.Critical, duration=8)
                self.iface.messageBar().pushMessage(
                        "Preview", "The query executed successfully but returned no results.", 
                        level=Qgis.Warning, duration=5)

            except Exception as e:
                self.iface.mainWindow().statusBar().clearMessage()
                self.iface.messageBar().pushMessage(
                    "Preview Failed", f"Error connecting to source: {str(e)}", 
                    level=Qgis.Critical, duration=8)

    def save_endpoint(self) -> None:
        """Persist the current endpoint URL to the endpoint configuration file."""
        path = self.get_endpoint_path()
        source = self.dlg.comboSourceType.currentText()
        try:
            with open(path, "r") as f:
                endpoints = json.load(f)
        except (FileNotFoundError, json.JSONDecodeError):
            endpoints = {}

        endpoints[source] = self.dlg.lineEndpoint.text()

        try:
            with open(path, "w") as f:
                json.dump(endpoints, f)
        except IOError as e:
            QgsMessageLog.logMessage(
                f'Could not save endpoint configuration: {e}', 'Triple2Layer', level=Qgis.Warning)

    def endpoint_defaut(self) -> None:
        """Load the last used endpoint URL for the selected source type."""
        path = self.get_endpoint_path()
        source = self.dlg.comboSourceType.currentText()
        try:
            with open(path, "r") as f:
                endpoints = json.load(f)
            self.dlg.lineEndpoint.setText(endpoints[source])
        except (FileNotFoundError, json.JSONDecodeError, KeyError):
            self.dlg.lineEndpoint.setText("")

    def get_endpoint_path(self) -> str:
        """Return the path to the endpoint configuration JSON file.

        Returns:
            Absolute path to endpoint.json inside the plugin directory.
        """
        return os.path.join(os.path.dirname(__file__), "endpoint.json")

    def import_from_triple(self) -> None:
            """Start an asynchronous task to import data from a SPARQL triple store."""
            QgsMessageLog.logMessage('Creating triple store import task...', 'Triple2Layer')
            
            # 1. Create the task object
            self.task = QgsTask.fromFunction(
                'Importing a layer', 
                self.load_triple_store, 
                on_finished=partial(self.create_layer, 'triple')
            )

            # 2. Connect signals BEFORE adding to the manager (Task 3 - Progress Feedback)
            self.task.progressChanged.connect(self.update_status_bar)
            self.task.taskCompleted.connect(self.check_if_imported_layer)
            
            # 3. Add to the Task Manager (only once!)
            QgsApplication.taskManager().addTask(self.task)

    def import_layer(self) -> None:
        """Route the import to the correct data source based on the user's selection."""

        self.erroOnLoadLayer = False

        source = self.dlg.comboSourceType.currentText()

        self.dlg.close()

        self.iface.messageBar().pushMessage(
                "Importing a layer", "Importing a layer ...",
                level=Qgis.Info, duration=10)

        if source == "Triple Store Endpoint":
            self.import_from_triple()
        else:
            self.import_from_dataworld()

    def open_sparql(self) -> None:
        """Open a file dialog to select a SPARQL query file and populate the attribute table."""
        self.file_name = str(QFileDialog.getOpenFileName(
            caption="Defining input file", filter="SPARQL(*.sparql)")[0])
        self.dlg.lineSPARQL.setText(self.file_name)

        try:
            with open(self.file_name, 'r') as file:
                data = file.read()
                self.sparql = data
                self.fill_table(data)
        except FileNotFoundError:
            QgsMessageLog.logMessage(
                f'SPARQL file not found: {self.file_name}', 'Triple2Layer', level=Qgis.Warning)
        except IOError as e:
            QgsMessageLog.logMessage(
                f'Could not read SPARQL file: {e}', 'Triple2Layer', level=Qgis.Warning)

    def geo_selected(self, row: int, state: int) -> None:
        """Enable or disable attribute name/type fields when a geometry column is selected.

        Args:
            row: The table row index of the checkbox that changed.
            state: The new checkbox state (Qt.Checked or Qt.Unchecked).
        """
        w1 = self.dlg.tableAttributes.cellWidget(row, 4)
        w2 = self.dlg.tableAttributes.cellWidget(row, 5)
        if state == Qt.Checked:
            w1.setEnabled(False)
            w2.setEnabled(False)
            self.dlg.pushButton.setEnabled(True)
        else:
            w1.setEnabled(True)
            w2.setEnabled(True)
            self.dlg.pushButton.setEnabled(False)
        

    def fill_table(self, s):
        """Parse SPARQL query variables and populate the attribute configuration table.

        Uses a regex to extract variables from the SELECT clause, which handles
        tabs, multiple spaces, DISTINCT/REDUCED keywords, and subqueries reliably.
        """
        match = re.search(r'SELECT\s+(DISTINCT\s+|REDUCED\s+)?(.*?)\s+WHERE', s, re.IGNORECASE | re.DOTALL)
        if not match:
            QgsMessageLog.logMessage(
                'Could not parse SPARQL query: SELECT...WHERE clause not found.',
                'Triple2Layer', level=Qgis.Warning)
            self.iface.messageBar().pushMessage(
                "Warning", "Could not parse the SPARQL file. Check the SELECT...WHERE syntax.",
                level=Qgis.Warning, duration=5)
            return

        variables_str = match.group(2)

        # Handle SELECT * wildcard
        if variables_str.strip() == '*':
            QgsMessageLog.logMessage(
                'SELECT * is not supported. Please list variables explicitly.',
                'Triple2Layer', level=Qgis.Warning)
            self.iface.messageBar().pushMessage(
                "Warning", "SELECT * is not supported. Please list variables explicitly.",
                level=Qgis.Warning, duration=5)
            return

        attributes = re.findall(r'\?(\w+)', variables_str)

        if not attributes:
            QgsMessageLog.logMessage(
                'No variables found in SELECT clause.', 'Triple2Layer', level=Qgis.Warning)
            return

        self.dlg.tableAttributes.setRowCount(len(attributes))
        self.dlg.tableAttributes.setColumnCount(6)
        self.dlg.tableAttributes.setHorizontalHeaderLabels(
            ["Import?", "IDColumn?", "GeoColumn?", "Variable", "Attribute name", "Attribute type"])

        for i, attr in enumerate(attributes):
            self.dlg.tableAttributes.setCellWidget(i, 0, QCheckBox())
            self.dlg.tableAttributes.setCellWidget(i, 1, QCheckBox())
            geo_check = QCheckBox()
            geo_check.stateChanged.connect(partial(self.geo_selected, i))
            self.dlg.tableAttributes.setCellWidget(i, 2, geo_check)
            self.dlg.tableAttributes.setItem(i, 3, QTableWidgetItem(attr))
            self.dlg.tableAttributes.setCellWidget(i, 4, QLineEdit(attr))
            comboBox = QComboBox()
            comboBox.addItem("String")
            comboBox.addItem("Int")
            comboBox.addItem("Double")
            self.dlg.tableAttributes.setCellWidget(i, 5, comboBox)

    def close(self) -> None:
        """Hide the plugin dialog without destroying it."""
        self.dlg.setVisible(False)